package ch06.sec03;

public class Goods {
	
	// 필드
	private String name;
	private int price;
	
	// 생성자
	public Goods() {};
	
	
	// 메소드
	public String getName() {
		return this.name;
	}
	
	public void setName(String name) {
		this.name = name;
	}
	
	
	public int getPrice() {
		return this.price;
	}
	
	public void setPrice(int price) {
		this.price = price;
	}
	
	

}
